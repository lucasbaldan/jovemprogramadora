package jovemprogramadorajava;


public class lacoFOR {
    public static void main(String[] args) {
        System.out.println("CONTANDO ATE 10");
        for(int i = 1; i<=10; i++){
            System.out.println("numero atual: " + i);
        }
        System.out.println("CONTANDO ATE 100");
        for(int i = 1; i<=100; i++){
            System.out.println("numero atual: " + i);
        }
        System.out.println("CONTANDO ATE 100000");
        for(int i = 1; i<=100000; i++){
            System.out.println("numero atual: " + i);
        }
        
    }
    
}
