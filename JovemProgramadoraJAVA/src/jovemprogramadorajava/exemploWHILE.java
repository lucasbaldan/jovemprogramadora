package jovemprogramadorajava;

public class exemploWHILE {

    public static void main(String[] args) {
        int contador = 1;

        while (contador <= 10) {
            System.out.print(contador + " ");
            contador++;
        }
        System.out.println("\n");
        
        contador = 1;
        while (contador <= 100) {
            System.out.print(contador + " ");
            contador++;
        }
    }

}
